def install_requirements():
    """Install required packages"""
    print("\nğŸ“¦ Installing required packages...")
    
    requirements = [
        "fastapi>=0.104.0",
        "uvicorn>=0.24.0",
        "httpx>=0.25.0",
        "python-multipart>=0.0.6",
        "jinja2>=3.1.2",
        "python-dotenv>=1.0.0"
    ]
    
    for requirement in requirements:
        try:
            print(f"   Installing {requirement}...")
            subprocess.check_call([sys.executable, "-m", "pip", "install", requirement], 
                                stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
            print(f"   âœ… {requirement} installed")
        except subprocess.CalledProcessError:
            print(f"   âŒ Failed to install {requirement}")
            return False
    
    return True

def setup_environment():
    """Setup environment variables"""
    print("\nğŸ”§ Setting up environment...")
    
    env_file = Path(".env")
    env_vars = {}
    
    # Load existing .env if it exists
    if env_file.exists():
        with open(env_file, 'r') as f:
            for line in f:
                if '=' in line and not line.startswith('#'):
                    key, value = line.strip().split('=', 1)
                    env_vars[key] = value
    
    # OpenAI API Key
    if 'OPENAI_API_KEY' not in env_vars or env_vars['OPENAI_API_KEY'] == 'your-openai-key-here':
        print("\nğŸ”‘ OpenAI API Key Setup")
        print("   To enable real AI responses, you need an OpenAI API key.")
        print("   Get one at: https://platform.openai.com/api-keys")
        
        api_key = input("\n   Enter your OpenAI API key (or press Enter to skip): ").strip()
        if api_key:
            env_vars['OPENAI_API_KEY'] = api_key
            print("   âœ… OpenAI API key configured")
        else:
            env_vars['OPENAI_API_KEY'] = 'your-openai-key-here'
            print("   âš ï¸  Skipped - Zeeky will use enhanced simulation mode")
    else:
        print("   âœ… OpenAI API key already configured")
    
    # Other optional API keys
    optional_keys = {
        'ANTHROPIC_API_KEY': 'Anthropic Claude API (optional)',
        'GOOGLE_API_KEY': 'Google Gemini API (optional)',
        'COHERE_API_KEY': 'Cohere API (optional)'
    }
    
    for key, description in optional_keys.items():
        if key not in env_vars:
            env_vars[key] = ''
    
    # Write .env file
    with open(env_file, 'w') as f:
        f.write("# Zeeky AI Environment Configuration\n")
        f.write("# Generated by setup_zeeky.py\n\n")
        for key, value in env_vars.items():
            f.write(f"{key}={value}\n")
    
    print(f"   âœ… Environment file created: {env_file}")
    return True

